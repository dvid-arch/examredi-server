export const chatWithAI = (req, res) => {
    const { message } = req.body;
    res.json({
        success: true,
        message: 'Chat response generated successfully',
        data: {
            reply: `This is a response from the local ExamRedi AI. You asked: "${message}". I'm here to help you study more effectively!`
        },
        statusCode: 200
    });
};

